package String;

public class SunStrinng {
    public static void main(String[] args) {
        String s = "abcdef";
        //substring(i,j)
        // i : include & j : Exclude
        //i to j-1
        System.out.println(s.substring(0, 4));

        //substring(i)
        System.out.println(s.substring(0));
    }
}
